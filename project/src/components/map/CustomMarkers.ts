import L from 'leaflet';

const createCustomIcon = (color: string) => new L.Icon({
  iconUrl: `https://raw.githubusercontent.com/pointhi/leaflet-color-markers/master/img/marker-icon-2x-${color}.png`,
  shadowUrl: 'https://cdnjs.cloudflare.com/ajax/libs/leaflet/1.7.1/images/marker-shadow.png',
  iconSize: [25, 41],
  iconAnchor: [12, 41],
  popupAnchor: [1, -34],
  shadowSize: [41, 41]
});

export const startIcon = createCustomIcon('green');
export const endIcon = createCustomIcon('red');
export const waypointIcon = createCustomIcon('blue');
export const locationIcon = createCustomIcon('yellow');